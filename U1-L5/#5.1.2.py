# 5.1.2 定义两个列表，分别存储平年和闰年的每个月份的天数，按照月份打印这个月的天数
# 例：平年的 1 月有 31 天，闰年的 1 月有 31 天
common_year = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]
leap_year = [31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]
for i in range(0, 12):
    print("平年 %d 月有 %d 天" % (i + 1, common_year[i]))
print()
for i in range(0, 12):
    print("闰年 %d 月有 %d 天" % (i + 1, leap_year[i]))

